package part1;

public interface PrinterInterface {

	/**
	 * Displays the content of its integer buffer.
	 * Precedes the value with string ">> " to flag it as output from the program.
	 */
	
	/**
	 * Sets the value of the private data member.
	 * @param a
	 */
	public void setValue(int a);
	
	/**
	 * Prints the value of the private data member.
	 */
	public void printValue();
}
