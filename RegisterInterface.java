package part1;

public interface RegisterInterface {

	/**
	 * Stores an integer
	 */
	
	/**
	 * Sets the value stored in the Register object.
	 * @param input - the value to be stored.
	 */
	public void setValue(int input);
	
	/**
	 * Gets the value stored in the Register object.
	 * @return value
	 */
	public int getValue();
}
